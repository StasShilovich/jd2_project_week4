#ITEM
CREATE TABLE IF NOT EXISTS t_item(f_id INT NOT NULL AUTO_INCREMENT,f_name VARCHAR(80) NOT NULL, f_status VARCHAR(20) NOT NULL,f_deleted TINYINT NOT NULL, PRIMARY KEY (f_id));
#USER
CREATE TABLE IF NOT EXISTS t_user(f_id INT NOT NULL AUTO_INCREMENT,f_name VARCHAR(70) NOT NULL,f_password VARCHAR(63) NOT NULL,f_role_id INT NOT NULL, PRIMARY KEY (f_id));
#ROLE
CREATE TABLE IF NOT EXISTS t_role(f_id INT NOT NULL AUTO_INCREMENT,f_name VARCHAR(30) NOT NULL, PRIMARY KEY (f_id));
#PERMISSION
CREATE TABLE IF NOT EXISTS t_permission(f_id INT NOT NULL AUTO_INCREMENT,f_name VARCHAR(40) NOT NULL, PRIMARY KEY (f_id));
#ROLE_PERMISSION
CREATE TABLE IF NOT EXISTS t_role_permission(f_role_id INT NOT NULL AUTO_INCREMENT,f_permission_id VARCHAR(40) NOT NULL, PRIMARY KEY (f_role_id, f_permission_id))